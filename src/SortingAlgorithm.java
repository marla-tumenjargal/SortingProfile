/**
 * interface SortingAlgorithm with method sort[] that allows classes that implement it to declare how a given array should be sorted
 * and through which type of sorting algorithm
 */
public interface SortingAlgorithm {
    /**
     * class implements the body of the method, but is just an outline header for the type of algorithm sorting the programmer wants to use
     * @param array
     */
    void sort(int[] array);
}
